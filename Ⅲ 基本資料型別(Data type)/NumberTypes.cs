using System;

namespace Tutorial
{
	class DatatypeOfNumber
	{
		public static void Main(string[] args)
		{
			// 整數：          // initial value
			int integerA;     // 0
			short shortA;     // 0
			long longA;       // 0l
			// 可帶有小數：
			float floatA;     // 0.0f
			double doubleA;   // 0.0d
			decimal decimalA; // 0.0m  // 不太常用到
			
			/*== == == == == == == ==*/
			// 不常用到的datatype
			sbyte sbyteA;     // 0
			byte byteA;       // 0
			
			ushort ushortA;   // 0
			
			uint uintA;       // 0
			
			ulong ulongA;     // 0
		}
	}
}